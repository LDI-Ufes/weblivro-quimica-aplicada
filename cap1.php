<h3>Catalisadores</h3>

<p>Diversos catalisadores têm sido preparados e utilizados na fabricação de ácido sulfúrico, os mais conhecidos são os catalisadores baseados em metais de transição tais como vanádio. Em geral 3-7% de \(V_2O_5\) (pentóxido de vanádio) é impregnado em terra de diatomáceas. As indústrias Monsanto, American Cyanamid e Stauffer Chemical Co. são conhecidas mundialmente pela fabricação de catalisadores.</p>

<p>Atualmente, existem catalisadores que atuam em fase líquida tais como: pirossulfato de vanádio suportado em sílica com promotores alcalinos. Sob condições de reação, 450 – 610 ºC, o componente ativo do catalisador (vanádio) se torna como um sal derretido formando uma camada muito fina de líquido na superfície do suporte de sílica (apenas 100 – 1000 Ǻ de espessura). A oxidação do \(SO_2\) a \(SO_3\) ocorre em ambos os sítios ativos localizados entre o filme líquido e a fronteira do filme com a superfície do suporte de sílica. </p>

<h3>Queimadores/Tratamento do gás do queimador</h3>

<p>O Enxofre é recebido e armazenado na forma líquida, posteriormente ele é transferido por meio de bombas do tanque de armazenamento para uma fornalha, na qual ele é nebulizado, conforme Figura 1.3. A porcentagem em geral liberada de \(SO_2\) pelo processo de queima do \(S\) está próxima de 9%, mas pode ser aumentada se for preciso. Quando se queima sulfetos, é necessário acrescentar coletores eficientes de poeira, resfriadores e torres de depuração, além de precipitadores de névoa ácida.</p>

<h3>Conversores</h3>
<p>Existem conversores de passos múltiplos onde a conversão global se torna bastante elevada, próximo a 98% ou mais. Sendo que para se obter uma conversão elevada é preciso abaixar a temperatura sucessivamente entre os estágios do conversor. A Figura 1.5 mostra um típico conversor de quatro etapas.</p>
